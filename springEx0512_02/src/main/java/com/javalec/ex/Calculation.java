package com.javalec.ex;

public class Calculation {

	public void add(int f,int s) {
		int result=f+s;
		System.out.println(f+"+"+s+"="+result);
	}
	public void sub(int f,int s) {
		int result=f-s;
		System.out.println(f+"-"+s+"="+result);
	}
	public void multi(int f,int s) {
		int result=f*s;
		System.out.println(f+"*"+s+"="+result);
	}
	public void div(int f,int s) {
		int result=f/s;
		System.out.println(f+"/"+s+"="+result);
	}
	
}
